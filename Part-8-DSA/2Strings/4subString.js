// A substring is a contiguous sequence of characters within a string.
//==========================================================================
// Generate ALL Substrings
function getAllSubstrings(s) {
  let all = [];

  for (let i = 0; i < s.length; i++) {
    for (let j = i + 1; j <= s.length; j++) {
      all.push(s.slice(i, j));
    }
  }

  return all;
}

