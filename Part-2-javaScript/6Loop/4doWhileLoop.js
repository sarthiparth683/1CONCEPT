// doWhile Loop -  Runs at least once, even if condition is false
let sum = 0;
let i = 1;
do {
  sum += i; 
  i++;
} while (i <= 5);
console.log(sum);
//----------------------------------------------------------
let count = 1;
do {
  console.log(count);
  count++;
} while (count <= 5);
