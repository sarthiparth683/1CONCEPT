// 6. Compare two Numbers
function compareTwoNumbers(num1, num2) {
    if (num1 > num2) {
        console.log("true");
    } else {
        console.log("false");
    }
    if (num1 < num2) {
        console.log("true");
    } else {
        console.log("false");
    }
    if (num1 == num2) {
        console.log("true");
    } else {
        console.log("false");
    }
};
compareTwoNumbers(8, 5);