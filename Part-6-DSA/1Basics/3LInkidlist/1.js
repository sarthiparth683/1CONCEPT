class Node {
  constructor(value) {
    this.value = value;
    this.next = null;
  }
}

class LinkedList {
  constructor() {
    this.head = null;
  }

  append(value) {
    let newNode = new Node(value);
    if (this.head === null) {
      this.head = newNode;
      return;
    }
    let current = this.head;  //  value
    while (current.next !== null) {
      current = current.next;
    }
    current.next = newNode;
  }
}
let list = new LinkedList();
list.append(5);
list.append(10);
list.append(15);
console.log(list);

